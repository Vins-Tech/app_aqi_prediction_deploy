def hist_aqi_avg(df, day_offset):
    results = []

    df['month']=df['date'].dt.month
    df['day']=df['date'].dt.day
    df['year']=df['date'].dt.year
    
    for idx, row in df.iterrows():
        target_date = row["date"] + pd.Timedelta(days=day_offset)

        hist = df[
            (df["month"] == target_date.month) &
            (df["day"] == target_date.day) &
            (df["year"] < row["year"])
        ]["aqipm25"]

        results.append(hist.mean() if len(hist) > 0 else np.nan)

    return results

def get_aqi_features(target_date,today_aqi):

    # get raw data
    df=call_sheets()
    df['date']=pd.to_datetime(df['date'])
    df=df.sort_values('date')

    # update todays date
    target_date=pd.to_datetime(target_date)
    today_date = target_date - timedelta(days=1)
    df = pd.concat([
    df,
    pd.DataFrame({
        "date": [today_date],
        "aqipm25": [today_aqi]
    })
    ])
    df = pd.concat([
    df,
    pd.DataFrame({
        "date": [target_date],
        "aqipm25": [-1]
    })
    ])
    df=df.drop_duplicates(subset=['date'],keep='first')
    df=df.reset_index(drop=True)
    
    #lag
    for lag in [1, 2, 3, 5, 7, 14, 21, 30]:
        df[f"aqi_lag_{lag}"] = df["aqipm25"].shift(lag)

    #roll
    for w in [3, 7, 14, 30]:
        df[f"aqi_roll_mean_{w}"] = (
            df["aqipm25"]
            .shift(1)
            .rolling(w)
            .mean()
        )
        
        df[f"aqi_roll_std_{w}"] = (
            df["aqipm25"]
            .shift(1)
            .rolling(w)
            .std()
        )

    # roll minmax
    df["aqi_roll_min_7"] = (
        df["aqipm25"]
        .shift(1)
        .rolling(7)
        .min()
    )           

    df["aqi_roll_max_7"] = (
        df["aqipm25"]
        .shift(1)
        .rolling(7)
        .max()
    )
    
    #hist features
    df["aqi_hist_prev_day_avg"] = hist_aqi_avg(df.copy(), -1)
    df["aqi_hist_same_day_avg"] = hist_aqi_avg(df.copy(), 0)
    df["aqi_hist_next_day_avg"] = hist_aqi_avg(df.copy(), 1)

    #final
    df=df[df['date']==target_date]
    df=df[['date']+aqi_cols]

    df=df.copy()
    return df
    
