def get_date_features(target_date):

    # create basic df
    df=pd.DataFrame({
        "date": [target_date]
    })
    df['date']=pd.to_datetime(df['date'])

    df["year"] = df["date"].dt.year
    df["month"] = df["date"].dt.month
    df["dayofweek"] = df["date"].dt.dayofweek
    df["dayofyear"] = df["date"].dt.dayofyear
    df["weekofyear"] = df["date"].dt.isocalendar().week.astype(int)
    df["is_weekend"] = (df["dayofweek"] >= 5).astype(int)

    # Cyclical encoding
    df["month_sin"] = np.sin(2 * np.pi * df["month"] / 12)
    df["month_cos"] = np.cos(2 * np.pi * df["month"] / 12)

    df["doy_sin"] = np.sin(2 * np.pi * df["dayofyear"] / 365)
    df["doy_cos"] = np.cos(2 * np.pi * df["dayofyear"] / 365)

    # hloiday
    india_holidays = holidays.India()
    df["is_holiday"] = df["date"].apply(
        lambda x: 1 if x in india_holidays else 0
    )
    df["is_pre_holiday"] = (
        df["date"] + pd.Timedelta(days=1)
    ).dt.date.isin(india_holidays).astype(int)

    df["is_post_holiday"] = (
        df["date"] - pd.Timedelta(days=1)
    ).dt.date.isin(india_holidays).astype(int)

    #final
    df=df[df['date']==target_date]
    df=df[['date']+date_cols]

    df=df.copy()
    return df

def get_all_features(target_date,todays_aqi):
    try:
        df1=get_weather_features(target_date)
        df2=get_aqi_features(target_date=target_date,today_aqi=todays_aqi)
        df3=get_date_features(target_date=target_date)

        df = df1.merge(df2, on="date", how="inner")
        df = df.merge(df3, on="date", how="inner")

         # Final validations
        if df.empty:
            raise ValueError("Final feature dataframe is empty")

        if df.isna().any().any():
            raise ValueError("NaNs detected in final features")

        df = df.copy()
        return df
    
    except Exception as e:
        raise RuntimeError(
            "Failed to generate model input features. "
            "Check weather API, AQI data, or input values."
        ) from e

